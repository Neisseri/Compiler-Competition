int main()
{
    // 直接常量赋值
    int a = 10;
    int b = 20;

    // 可以通过常量传播优化的表达式
    int c = a + b;  // 期望优化为 int c = 30;
    int d = a * 2;  // 期望优化为 int d = 20;
    int e = b - 10; // 期望优化为 int e = 10;

    // 依赖于前面优化的结果
    int f = c + d; // 期望优化为 int f = 50;

    // 条件语句
    if (a == 10)
    {
        e = e + 1; // 由于a是常量且等于10，这部分代码应该保留。
    }
    else
    {
        e = 100; // 这部分代码永远不会执行，理论上可以被优化掉。
    }
    return e;
}